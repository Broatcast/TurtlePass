<?php

namespace UniqueLibs\QueryBuilderOperationBundle\Filter;

abstract class AbstractFilter implements FilterInterface
{
    /**
     * @var string
     */
    protected $name;

    /**
     * @param string $name
     */
    public function __construct($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
